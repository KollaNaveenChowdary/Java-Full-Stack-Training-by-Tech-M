// For Loop
for (let i = 21; i <= 40; i++) {
    if (i % 2 === 0) {
        console.log(i);
    }
}

// While Loop
let i = 11;
while (i <= 15) {
    console.log(i);
    i++;
}

// Do-While Loop
let j = 21;
do {
    if (j % 4 === 0)
        console.log(j);
    j++;
} while (j <= 35);
