// Return Statement
function add(a, b) {
    return a + b;
}
const res = add(55, 63);
console.log(res);

// Break and Continue Statements
for (let k = 1; k <= 5; k++) {
    if (k === 3) break;
    console.log(k);
}

for (let l = 1; l <= 5; l++) {
    if (l === 3) continue;
    console.log(l);
}
